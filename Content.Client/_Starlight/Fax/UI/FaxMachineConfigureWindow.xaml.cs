using System.Linq;
using Content.Client.UserInterface.Controls;
using Content.Shared._Starlight.Fax;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._Starlight.Fax.UI;

[GenerateTypedNameReferences]
public sealed partial class FaxMachineConfigureWindow : FancyWindow
{
    public Action? OnSubmit;

    private const int NoneItemId = -1;

    public string CurrentName
    {
        get => NameEdit.Text;
        set => NameEdit.Text = value;
    }

    public int CurrentOrder
    {
        get => OrderSpinBox.Value;
        set => OrderSpinBox.Value = value;
    }

    public string? CurrentGroup => GroupSelector.SelectedMetadata as string;

    public FaxMachineConfigureWindow()
    {
        RobustXamlLoader.Load(this);
        SubmitButton.OnPressed += _ => OnSubmit?.Invoke();
    }

    public void SetGroupings(
        IEnumerable<FaxGroupPrototype> groups,
        ProtoId<FaxGroupPrototype>? selectedGroup,
        ProtoId<FaxGroupPrototype>? intrinsicGroup,
        bool intrinsicLocked,
        bool emagged)
    {
        GroupSelector.Clear();
        GroupSelector.AddItem(Loc.GetString("fax-machine-configure-ui-group-none"), NoneItemId);

        foreach (var group in groups.OrderBy(g => g.Order))
        {
            // If this machine is locked to its intrinsic group and not emagged, only the intrinsic group is shown.
            if (intrinsicLocked && !emagged && group.ID != intrinsicGroup?.Id)
                continue;

            // Otherwise, to be able to select a group, one of these conditions must be met:
            // - The group is Selectable by default;
            // - The group is SelectableEmagged, and the machine is emagged;
            // - The group is currently selected;
            // - The group is intrinsic to the machine.
            if (!(group.Selectable ||
                  (emagged && group.SelectableEmagged) ||
                  group.ID == selectedGroup?.Id ||
                  group.ID == intrinsicGroup?.Id))
                continue;

            GroupSelector.AddItem(Loc.GetString(group.Name), group.ID.GetHashCode());
            GroupSelector.SetItemMetadata(GroupSelector.ItemCount - 1, group.ID);
            GroupSelector.SetItemColor(group.Color);
        }

        GroupSelector.SelectId(selectedGroup is null ? NoneItemId : selectedGroup.Value.Id.GetHashCode());
    }

}
