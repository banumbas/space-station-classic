using Content.Client.UserInterface.Controls;
using Content.Shared._Starlight.Devil;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._Starlight.Devil.Ui;

[GenerateTypedNameReferences]
public sealed partial class DevilDamnationsMenu : FancyWindow
{
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    public DevilDamnationsMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void Update(DevilDamnationsBuiState state)
    {
        DamnationsDisplayText.Orphan();
        DamnationsDisplayContainer.Children.Clear();
        DamnationsDisplayContainer.Children.Add(DamnationsDisplayText);

        List<DamnationPrototype> damnations = new();
        foreach (var damnationId in state.Damnations)
            if (_prototypeManager.TryIndex(damnationId, out var damnation)) damnations.Add(damnation);

        damnations.Sort((x, y) => x.Cost.CompareTo(y.Cost));

        foreach (var damnation in damnations)
        {
            var element = new DevilDamnationDisplay(damnation);
            DamnationsDisplayContainer.AddChild(element);
        }
    }
}
