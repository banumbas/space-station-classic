using System.Linq;
using System.Numerics;
using System.Text;
using Content.Shared._Starlight.Achievement;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client._Starlight.Achievement;

[GenerateTypedNameReferences]
public sealed partial class AchievementWindow : DefaultWindow
{
    [Dependency] private readonly IEntitySystemManager _entitySystems = default!;
    private const string AllCategoryId = "__all";
    // probably should move this to own style
    private static readonly Color _frameBackgroundColor = Color.FromHex("#1B1712");
    private static readonly Color _outerBackgroundColor = Color.FromHex("#2B261F");
    private static readonly Color _headerBackgroundColor = Color.FromHex("#8B6B2E");
    private static readonly Color _headerBorderColor = Color.FromHex("#E1BF62");
    private static readonly Color _panelBackgroundColor = Color.FromHex("#241E17");
    private static readonly Color _innerPanelColor = Color.FromHex("#3A3126");
    private static readonly Color _borderDarkColor = Color.FromHex("#100D08");
    private static readonly Color _borderMidColor = Color.FromHex("#5A4A34");
    private static readonly Color _accentGoldColor = Color.FromHex("#FFD83D");
    private static readonly Color _softGoldColor = Color.FromHex("#E0D0A4");
    private static readonly Color _bodyTextColor = Color.FromHex("#D8D8D8");
    private static readonly Color _successTextColor = Color.FromHex("#8BE46B");
    private static readonly Color _lockedTextColor = Color.FromHex("#B7A688");
    private string _selectedCategory = AllCategoryId;
    private IPrototypeManager? _currentProtoManager;
    private IClientAchievementManager? _currentAchievements;

    public AchievementWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        ApplyStyles();
    }

    public void Populate(IPrototypeManager protoManager, IClientAchievementManager achievements)
    {
        _currentProtoManager = protoManager;
        _currentAchievements = achievements;
        PopulateCategories(protoManager, achievements);
        AchievementList.RemoveAllChildren();
        var spriteSystem = _entitySystems.GetEntitySystem<SpriteSystem>();
        var visibleAchievements = protoManager.EnumeratePrototypes<AchievementPrototype>()
            .Where(proto => (!proto.Hidden || achievements.IsUnlocked(proto.ID))
                && (_selectedCategory == AllCategoryId || proto.Category == _selectedCategory))
            .OrderBy(proto => proto.Category)
            .ThenBy(proto => Loc.GetString(proto.Name))
            .ToList();

        CategoryHeader.Text = GetCategoryDisplayName(_selectedCategory);
        CategorySummary.Text = Loc.GetString("achievement-window-category-summary", ("count", visibleAchievements.Count));

        if (visibleAchievements.Count == 0)
        {
            AchievementList.AddChild(MakeEmptyStateCard());
            return;
        }

        for (var i = 0; i < visibleAchievements.Count; i++)
        {
            var proto = visibleAchievements[i];
            var unlocked = achievements.IsUnlocked(proto.ID);
            AchievementList.AddChild(MakeAchievementCard(proto, achievements, spriteSystem, unlocked));
        }
    }

    private void ApplyStyles()
    {
        WindowFrame.PanelOverride = CreatePanelStyle(_frameBackgroundColor, _borderDarkColor, new Thickness(3));
        HeroPanel.PanelOverride = CreatePanelStyle(_headerBackgroundColor, _headerBorderColor, new Thickness(2));
        SidebarPanel.PanelOverride = CreatePanelStyle(_outerBackgroundColor, _borderDarkColor, new Thickness(2));
        SidebarHeaderPanel.PanelOverride = CreatePanelStyle(Color.FromHex("#5D4820"), _headerBorderColor, new Thickness(0, 0, 0, 2));
        SidebarBodyPanel.PanelOverride = CreatePanelStyle(_panelBackgroundColor, _borderMidColor, new Thickness(2));
        ContentPanel.PanelOverride = CreatePanelStyle(_outerBackgroundColor, _borderDarkColor, new Thickness(2));
        ContentHeaderPanel.PanelOverride = CreatePanelStyle(_innerPanelColor, _borderMidColor, new Thickness(2));
        ContentBodyPanel.PanelOverride = CreatePanelStyle(_panelBackgroundColor, _borderMidColor, new Thickness(2));
    }

    private void PopulateCategories(IPrototypeManager protoManager, IClientAchievementManager achievements)
    {
        var visibleAchievements = protoManager.EnumeratePrototypes<AchievementPrototype>()
            .Where(proto => !proto.Hidden || achievements.IsUnlocked(proto.ID))
            .ToList();

        var categories = visibleAchievements
            .Select(proto => proto.Category)
            .Distinct()
            .OrderBy(category => category)
            .ToList();

        if (_selectedCategory != AllCategoryId && !categories.Contains(_selectedCategory))
            _selectedCategory = AllCategoryId;

        CategoryList.RemoveAllChildren();
        CategoryList.AddChild(MakeCategoryButton(AllCategoryId, visibleAchievements.Count));

        foreach (var category in categories)
        {
            var count = visibleAchievements.Count(proto => proto.Category == category);
            CategoryList.AddChild(MakeCategoryButton(category, count));
        }
    }

    private Control MakeEmptyStateCard()
    {
        var panel = new PanelContainer
        {
            HorizontalExpand = true,
            PanelOverride = CreatePanelStyle(_innerPanelColor, _borderMidColor, new Thickness(2)),
        };

        var box = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            Margin = new Thickness(10, 10, 10, 10),
        };
        panel.AddChild(box);

        box.AddChild(new Label
        {
            Text = Loc.GetString("achievement-window-no-achievements-in-category"),
            FontColorOverride = _softGoldColor,
        });

        return panel;
    }

    private Button MakeCategoryButton(string category, int count)
    {
        var selected = category == _selectedCategory;
        var button = new Button
        {
            Text = $"{(selected ? "> " : string.Empty)}{GetCategoryDisplayName(category)} ({count})",
            HorizontalExpand = true,
            MinHeight = 34,
            StyleBoxOverride = CreateCategoryButtonStyle(selected),
            Modulate = selected ? Color.White : Color.FromHex("#F0E1B8"),
        };

        button.OnPressed += _ =>
        {
            _selectedCategory = category;
            if (_currentProtoManager != null && _currentAchievements != null)
                Populate(_currentProtoManager, _currentAchievements);
        };

        return button;
    }

    private Control MakeAchievementCard(AchievementPrototype proto, IClientAchievementManager achievements, SpriteSystem spriteSystem, bool unlocked)
    {
        var card = new PanelContainer
        {
            HorizontalExpand = true,
            PanelOverride = CreateAchievementCardStyle(unlocked),
        };

        var root = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            Margin = new Thickness(8, 8, 8, 8),
            SeparationOverride = 8,
        };
        card.AddChild(root);

        var topRow = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            SeparationOverride = 10,
        };

        var iconFrame = new PanelContainer
        {
            PanelOverride = CreatePanelStyle(Color.FromHex("#1A1611"), Color.FromHex(unlocked ? "#C9A64A" : "#6F5D3E"), new Thickness(2)),
        };

        var iconRect = new TextureRect
        {
            MinSize = new Vector2(52, 52),
            MaxSize = new Vector2(52, 52),
            Margin = new Thickness(6, 6, 6, 6),
            Stretch = TextureRect.StretchMode.KeepAspectCentered,
            Texture = proto.Icon != null ? spriteSystem.Frame0(proto.Icon) : null,
            Modulate = unlocked ? Color.White : Color.FromHex("#B6A58A"),
        };
        iconFrame.AddChild(iconRect);
        topRow.AddChild(iconFrame);

        var content = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            SeparationOverride = 5,
        };

        var titleRow = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            SeparationOverride = 8,
        };

        titleRow.AddChild(new Label
        {
            Text = Loc.GetString(proto.Name),
            HorizontalExpand = true,
            FontColorOverride = unlocked ? _accentGoldColor :_softGoldColor,
        });
        titleRow.AddChild(MakeStatusBadge(unlocked));
        content.AddChild(titleRow);

        var descLabel = new RichTextLabel
        {
            HorizontalExpand = true,
            Modulate = _bodyTextColor,
        };
        descLabel.SetMessage(FormattedMessage.FromUnformatted(Loc.GetString(proto.Description)));
        content.AddChild(descLabel);

        if (proto.ShowProgress && proto.Requirements.Count > 0)
        {
            foreach (var req in proto.Requirements)
            {
                var current = unlocked
                    ? req.RequiredProgress
                    : achievements.GetProgress(req.ProgressType);

                content.AddChild(MakeProgressRow(current, req.RequiredProgress, unlocked));
            }
        }

        topRow.AddChild(content);
        root.AddChild(topRow);
        return card;
    }

    private Control MakeStatusBadge(bool unlocked)
    {
        var panel = new PanelContainer
        {
            PanelOverride = CreatePanelStyle(
                unlocked ? Color.FromHex("#2F4A25") : Color.FromHex("#3B3328"),
                unlocked ? _successTextColor : Color.FromHex("#8A7A57"),
                new Thickness(2)),
        };

        panel.AddChild(new Label
        {
            Text = Loc.GetString(unlocked ? "achievement-window-status-unlocked" : "achievement-window-status-locked"),
            Margin = new Thickness(8, 3, 8, 3),
            FontColorOverride = unlocked ? _successTextColor : _lockedTextColor,
        });

        return panel;
    }

    private Control MakeProgressRow(double current, double required, bool unlocked)
    {
        var container = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            HorizontalExpand = true,
            SeparationOverride = 3,
            Margin = new Thickness(0, 2, 0, 0),
        };

        var cappedCurrent = required <= 0 ? 0
            : Math.Min(current, required);

        container.AddChild(new Label
        {
            Text = Loc.GetString("achievement-window-progress",
                ("current", $"{cappedCurrent:F0}"),
                ("required", $"{required:F0}")),
            FontColorOverride = _softGoldColor,
        });

        var bar = new ProgressBar
        {
            HorizontalExpand = true,
            MinSize = new Vector2(0, 14),
            MinValue = 0,
            MaxValue = (float)Math.Max(required, 1),
            Value = (float)Math.Min(cappedCurrent, Math.Max(required, 1)),
            BackgroundStyleBoxOverride = new StyleBoxFlat
            {
                BackgroundColor = Color.FromHex("#18140F"),
            },
            ForegroundStyleBoxOverride = new StyleBoxFlat
            {
                BackgroundColor = unlocked ? _successTextColor : Color.FromHex("#C9A64A"),
            }
        };
        container.AddChild(bar);

        return container;
    }

    private static StyleBoxFlat CreatePanelStyle(Color background, Color border, Thickness thickness) => new()
    {
        BackgroundColor = background,
        BorderColor = border,
        BorderThickness = thickness,
        ContentMarginLeftOverride = 2,
        ContentMarginTopOverride = 2,
        ContentMarginRightOverride = 2,
        ContentMarginBottomOverride = 2,
    };

    private static StyleBoxFlat CreateCategoryButtonStyle(bool selected) => new()
    {
        BackgroundColor = selected ? Color.FromHex("#6F5525") : Color.FromHex("#30281E"),
        BorderColor = selected ? _headerBorderColor : _borderMidColor,
        BorderThickness = new Thickness(2),
        ContentMarginLeftOverride = 8,
        ContentMarginTopOverride = 5,
        ContentMarginRightOverride = 8,
        ContentMarginBottomOverride = 5,
    };

    private static StyleBoxFlat CreateAchievementCardStyle(bool unlocked) => new()
    {
        BackgroundColor = unlocked ? _innerPanelColor : Color.FromHex("#2C261F"),
        BorderColor = unlocked ? Color.FromHex("#C9A64A") : _borderMidColor,
        BorderThickness = new Thickness(2),
        ContentMarginLeftOverride = 2,
        ContentMarginTopOverride = 2,
        ContentMarginRightOverride = 2,
        ContentMarginBottomOverride = 2,
    };

    private static string GetCategoryDisplayName(string category)
    {
        if (category == AllCategoryId)
            return Loc.GetString("achievement-window-category-all");

        if (string.IsNullOrWhiteSpace(category))
            return Loc.GetString("achievement-window-category-uncategorized");

        var builder = new StringBuilder(category.Length);
        var capitalizeNext = true;

        foreach (var ch in category)
        {
            if (ch is '_' or ' ')
            {
                if (builder.Length > 0 && builder[builder.Length - 1] != ' ')
                    builder.Append(' ');

                capitalizeNext = true;
                continue;
            }

            builder.Append(capitalizeNext ? char.ToUpperInvariant(ch) : ch);
            capitalizeNext = false;
        }

        return builder.ToString().TrimEnd();
    }
}
