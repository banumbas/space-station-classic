using Content.Shared._Starlight.Achievement;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.Utility;
using System.Numerics;

namespace Content.Client._Starlight.Achievement;

[GenerateTypedNameReferences]
public sealed partial class AchievementNotification : Control
{
    private const float DragHeaderHeight = 34f;
    private const float CloseButtonHitWidth = 40f;

    [Dependency] private readonly IEntitySystemManager _entitySystems = default!;
    private bool _dragging;
    private Vector2 _dragOffset;

    public event Action? CloseRequested;

    public AchievementNotification()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        MouseFilter = MouseFilterMode.Stop;

        ShadowPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = new Color(0, 0, 0, 110),
            ContentMarginLeftOverride = 3,
            ContentMarginTopOverride = 3,
            ContentMarginRightOverride = 3,
            ContentMarginBottomOverride = 3,
        };

        OuterPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#2B261F"),
            BorderColor = Color.FromHex("#100D08"),
            BorderThickness = new Thickness(3),
            ContentMarginLeftOverride = 2,
            ContentMarginTopOverride = 2,
            ContentMarginRightOverride = 2,
            ContentMarginBottomOverride = 2,
        };

        HeaderPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#8B6B2E"),
            BorderColor = Color.FromHex("#E1BF62"),
            BorderThickness = new Thickness(0, 0, 0, 2),
        };

        CloseButton.StyleBoxOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#6F5525"),
            BorderColor = Color.FromHex("#C9A64A"),
            BorderThickness = new Thickness(2),
            ContentMarginLeftOverride = 4,
            ContentMarginTopOverride = 2,
            ContentMarginRightOverride = 4,
            ContentMarginBottomOverride = 2,
        };
        CloseButton.Label.FontColorOverride = Color.FromHex("#F6E5A6");
        CloseButton.Modulate = Color.White;
        CloseButton.OnPressed += _ => CloseRequested?.Invoke();

        InnerPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#3A3126"),
            BorderColor = Color.FromHex("#5A4A34"),
            BorderThickness = new Thickness(2),
        };

        FooterPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#241E17"),
            BorderColor = Color.FromHex("#5A4A34"),
            BorderThickness = new Thickness(2),
        };

        IconFrame.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#1A1611"),
            BorderColor = Color.FromHex("#C9A64A"),
            BorderThickness = new Thickness(2),
        };
    }

    public void SetAchievement(AchievementPrototype proto)
    {
        var spriteSystem = _entitySystems.GetEntitySystem<SpriteSystem>();

        NotificationTitle.Text = Loc.GetString("achievement-notification-title");
        AchievementName.Text = Loc.GetString(proto.Name);
        AchievementDescription.SetMessage(FormattedMessage.FromUnformatted(Loc.GetString(proto.Description)));
        AchievementDescription.Modulate = Color.FromHex("#D8D8D8");
        FooterText.Text = Loc.GetString("achievement-notification-footer");

        IconTexture.Texture = proto.Icon != null
            ? spriteSystem.Frame0(proto.Icon)
            : null;
        IconTexture.Modulate = proto.Icon != null
            ? Color.White
            : Color.FromHex("#C9A64A");
    }

    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);

        if (args.Function != EngineKeyFunctions.UIClick)
            return;

        if (args.RelativePosition.Y > DragHeaderHeight)
            return;

        if (args.RelativePosition.X > Width - CloseButtonHitWidth)
            return;

        _dragging = true;
        _dragOffset = args.RelativePosition;
        args.Handle();
    }

    protected override void KeyBindUp(GUIBoundKeyEventArgs args)
    {
        base.KeyBindUp(args);

        if (args.Function != EngineKeyFunctions.UIClick)
            return;

        _dragging = false;
        args.Handle();
    }

    protected override void MouseMove(GUIMouseMoveEventArgs args)
    {
        base.MouseMove(args);

        if (!_dragging)
            return;

        LayoutContainer.SetPosition(this, args.GlobalPosition - _dragOffset);
    }
}
